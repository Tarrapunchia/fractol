#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	void	*buf;

	if (!s)
		return ;
	buf = s;
	i = 0;
	while (i < n)
	{
		((unsigned char *)(buf))[i] = '\0';
		i++;
	}
}
