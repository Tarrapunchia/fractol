#include "libft.h"

int	ft_atoi(const char *s)
{
	int		sign;
	long	slz;

	sign = 1;
	slz = 0;
	while (*s == ' ' || (*s >= 9 && *s <= 13))
		s++;
	if (*s == '+' || *s == '-')
	{
		if (!ft_isdigit(*(s + 1)))
			return (0);
		if (*s == '-')
			sign *= -1;
		s++;
	}
	while (*s >= '0' && *s <= '9' && *s != '\0')
	{
		slz = slz * 10 + (*s - '0');
		s++;
	}
	return (sign * (int)slz);
}
